#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t					i;
	unsigned char			*str;

	if (n > 0)
	{
		str = (unsigned char *)s;
		i = 0;
		while (i < n)
		{
			str[i] = 0;
			i++;
		}
	}
}
