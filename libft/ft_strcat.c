#include "libft.h"

char		*ft_strcat(char *dest, const char *str)
{
	int i;
	int c;

	i = 0;
	c = 0;
	while (dest[i] != '\0')
	{
		i++;
	}
	while (str[c] != '\0')
	{
		dest[i + c] = str[c];
		c++;
	}
	dest[i + c] = '\0';
	return (dest);
}
